// subtracts from balance
export const subtractFromBalanceService = (balance, amount) => balance - amount;

// adds to balance
export const addToBalanceService = (balance, amount) => balance + amount;


// adds to budget
export const addToBudgetService = (budget, amount) => budget + amount;

// subtracts from budget
export const subtractFromBudgetService = (budget, amount) => budget - amount;
